-module(marionet_json).

%% API
-export([encode/1, decode/1]).

%%%===================================================================
%%% API
%%%===================================================================

%%--------------------------------------------------------------------
%% @doc Encode term() to JSON
%% @end
%%--------------------------------------------------------------------
-spec encode(term()) -> binary().
encode(Obj) ->
    jsx:encode(Obj).

%%--------------------------------------------------------------------
%% @doc Decode JSON to term()
%% @end
%%--------------------------------------------------------------------
-spec decode(binary) -> term().
decode(Bin) ->
    jsx:decode(Bin).

